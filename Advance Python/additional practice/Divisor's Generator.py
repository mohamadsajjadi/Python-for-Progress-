# در این بخش از شما می‌خواهیم تا تابعی بنویسید که تمام مقسوم علیه‌های یک عدد را از کوچک به بزرگ در قالب یک تولیدکننده برگرداند. شما باید تابع divs را طوری بنویسید که با ورودی گرفتن عدد aa، مقسوم‌علیه‌های aa را از کوچک به بزرگ در قالب یک generator خروجی دهد.
def divs(a):
    for i in range(1, a + 1):
        if a % i == 0:
            yield i


print(list(divs(6)))
